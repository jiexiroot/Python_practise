numstr = '0123456789'
# 1、从2到5开始切片，步长为1
print(numstr[2:5:1])
# 简写
print(numstr[2:5])
# 2、只有的结尾的字符串切片: 代表从索引为0开始，截取到结尾字符-1的位置
print(numstr[:5])
# 3、只有开头的字符串切片: 代表从起始位置开始，已知截取到字符串的结尾
print(numstr[1:])
# 4、获取或拷贝整个字符串
print(numstr[:])
# 5、调整步阶
print(numstr[::2])
# 6、把步阶设置为负整数
print(numstr[::-1])
# 7、起始位置与结束位置都是负数（要遵循一个原则：必须是从左向右截取）
print(numstr[-4:-1])
# 8、结束字符为负数，如截取012345678
print(numstr[:-1])
